// Equals to ==
let a = 10;
let b= "10";
let c = a==b;
console.log(c);

// Equal value and Equal type ===
let d = 20;
let e = 22;
let f = d===e;
console.log(f);

// Not Equal !=
let g = 10;
let h = 22;
let i = g!=h;
console.log(i);

// Not equal value or not equal type !==
let j = 18;
let k = 18;
let l = j!==k;
console.log(l);

// greater than >
let m = 30;
let n = 40;
let o = m>n
console.log(o);

// less than <
let p = 40;
let q = 41;
let r = p<q;
console.log(r);

// greater than or equals to >=
let s = 33;
let t =22;
let w = s>=t;
console.log(w);

// less thn or equals to <=
let x = 33;
let y = 22;
let z = x<=y;
console.log(z);

